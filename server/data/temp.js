function getAllShowIds(data) {
    let showIds = new Set();
    data.forEach(element => {
        element.timings.forEach(ele => {
            ele.theaters.forEach(ele => {
                ele.timings.forEach((ele) => {
                    showIds.add(ele.showid);
                });
            });
        });
    });

    return Array.from(showIds);
}

